package secondweekjava;
//prg on arthematic operations 
import java.util.*;
public class arthematicop {
public static void main(String args[])
{
	Scanner sc=new Scanner(System.in);
	int a=sc.nextInt();
	int b=sc.nextInt();
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a%b);
	System.out.println(a/b);


}
}
